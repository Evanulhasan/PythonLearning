# Typecasting in python 
"""Type casting in Python is
the process of converting a value
from one data type to another to 
ensure compatibility and perform 
specific operations. Python supports
two main types of casting: implicit 
(automatic) and explicit (manual)."""

# Explicit 
"""Explicit type casting 
also called type conversion,
is done manually by the programmer
using built in functions to convert 
data to a required type."""
